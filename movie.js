function addToWatchLater(title, description, imageSource) {
  // Get current watch later list from localStorage or initialize as an empty array
  let watchLater = JSON.parse(localStorage.getItem('watchlater')) || [];

  // Check if the movie is already in the watch later list
  const existingMovie = watchLater.find(movie => movie.title === title);
  if (!existingMovie) {
    // Add the movie to the watch later list
    watchLater.push({ title, description, imageSource });

    // Update localStorage with the updated watch later list
    localStorage.setItem('watchlater', JSON.stringify(watchLater));

    // Display a message or perform any other action to confirm the addition to the watch later list
    alert(`${title} added to watch later.`);
  } else {
    // Movie is already in the watch later list, display a message or handle it accordingly
    alert(`${title} is already in watch later.`);
  }
}




